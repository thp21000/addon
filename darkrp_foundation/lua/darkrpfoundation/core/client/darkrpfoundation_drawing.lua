--[[ DRAWING FUNCTIONS ]]--
DarkRPFoundation.DRAW = {}

function DarkRPFoundation.DRAW.ThemedBox( X, Y, W, H, H2, Alpha )
	local Alph = 255
	if( Alpha ) then
		Alph = Alpha
	end
	
	--Background Bar
	surface.SetDrawColor( 113, 113, 113, Alph )
	surface.DrawRect( X, Y, W, H )		
	
	surface.SetDrawColor( 127, 127, 127, Alph )
	surface.DrawRect( X, Y, W, H2 )	
	
	--Outline of Bar
	surface.SetDrawColor( Color( 0, 0, 0, Alph ) )
	surface.DrawOutlinedRect( X, Y, W, H )
end

function DarkRPFoundation.DRAW.IconBox( X, Y, W, H, Mat, Padding, Alpha )
	local Alph = 255
	if( Alpha ) then
		Alph = Alpha
	end
	
	local Padd = 2
	if( Padding ) then
		Padd = Padding
	end
	
	surface.SetDrawColor( 167, 167, 167, Alph )
	surface.DrawRect( X, Y, W, H )	
	
	surface.SetDrawColor( 255, 255, 255, Alph )
	surface.SetMaterial( Mat )
	surface.DrawTexturedRect( X+Padd, Y+Padd, W-(2*Padd), H-(2*Padd) )
end

function DarkRPFoundation.DRAW.OutlinedBox( x, y, w, h, BorderW, Col )
	surface.SetDrawColor( Col )
	
	for i = 0, BorderW-1 do
		surface.DrawOutlinedRect( x+i, y+i, w-(2*i), h-(2*i) )
	end
end

function DarkRPFoundation.DRAW.GradientOutlinedBox( x, y, w, h, BorderW, Col )
	surface.SetAlphaMultiplier( 0.5 )
	surface.SetDrawColor( Col )
	surface.DrawRect( x, y, w, h )
	surface.SetAlphaMultiplier( 1 )
	
	local BorderGradAlpha = 150
	surface.SetAlphaMultiplier( 1 )
	-- Top --
	surface.SetDrawColor( Col )
	surface.DrawRect( x, y, w, BorderW )

	surface.SetDrawColor( 255, 255, 255, BorderGradAlpha )
	surface.SetMaterial( DarkRPFoundation.MATERIALS.GradientRight )
	surface.DrawTexturedRect( x, y, w, BorderW )
	
	-- Right --
	surface.SetDrawColor( Col )
	surface.DrawRect( x+w-BorderW, y+BorderW, BorderW, h-BorderW )

	surface.SetDrawColor( 255, 255, 255, BorderGradAlpha )
	surface.SetMaterial( DarkRPFoundation.MATERIALS.GradientUp )
	surface.DrawTexturedRect( x+w-BorderW, y+BorderW, BorderW, h-BorderW )
	
	-- Bottom --
	surface.SetDrawColor( Col )
	surface.DrawRect( x, y+h-BorderW, w-BorderW, BorderW )

	surface.SetDrawColor( 255, 255, 255, BorderGradAlpha )
	surface.SetMaterial( DarkRPFoundation.MATERIALS.GradientLeft )
	surface.DrawTexturedRect( x, y+h-BorderW, w-BorderW, BorderW )
	
	-- Left --
	surface.SetDrawColor( Col )
	surface.DrawRect( x, y+BorderW, BorderW, h-BorderW-BorderW )

	surface.SetDrawColor( 255, 255, 255, BorderGradAlpha )
	surface.SetMaterial( DarkRPFoundation.MATERIALS.GradientDown )
	surface.DrawTexturedRect( x, y+BorderW, BorderW, h-BorderW-BorderW )
	surface.SetAlphaMultiplier( 1 )
end

function DarkRPFoundation.DRAW.GetTheme( Type, Alpha )
	if( DarkRPFoundation.CONFIG.THEME[Type] and IsColor( DarkRPFoundation.CONFIG.THEME[Type] ) ) then
		return Color( DarkRPFoundation.CONFIG.THEME[Type].r, DarkRPFoundation.CONFIG.THEME[Type].g, DarkRPFoundation.CONFIG.THEME[Type].b, Alpha )
	else
		return Color( 255, 255, 255, Alpha )
	end
end

--[[ DERMA FUNCTIONS ]]--
DarkRPFoundation.DERMA = {}

function DarkRPFoundation.DERMA.StringRequest( title, subtitle, default, func_confirm, func_cancel, confirmText, cancelText )
	local DermaPanel = vgui.Create( "DFrame" )
	DermaPanel:SetPos( 0, 0 )
	DermaPanel:SetSize( ScrW(), ScrH() )
	DermaPanel:SetTitle( "" )
	DermaPanel:SetDraggable( false )
	DermaPanel:MakePopup()
	DermaPanel.Paint = function() end
	
	local BackPanel = vgui.Create( "DPanel", DermaPanel )
	BackPanel:SetSize( (1920)*0.25, (1080)*0.2 )
	BackPanel:Center()
	BackPanel.Paint = function( self, w, h )
		surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "PrimaryColor" ) )
		surface.DrawRect( 0, 0, w, h )
		
		surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "SecondaryColor" ) )
		surface.DrawRect( 0, 0, w, 45 )	
		
		draw.SimpleText( title, "DarkRPFoundation_Font_Lvl_RewardHeader", 15, 45/2, Color( 245, 245, 245 ), 0, TEXT_ALIGN_CENTER )
		draw.SimpleText( subtitle, "DarkRPFoundation_Font_Lvl_PlyNameXXXS", w/2, 60, Color( 245, 245, 245 ), TEXT_ALIGN_CENTER, 0 )
	end
	
	local MenuCloseButton = vgui.Create( "DButton", BackPanel )
	local ButSize = 25
	MenuCloseButton:SetSize( ButSize, ButSize )
	MenuCloseButton:SetPos( BackPanel:GetWide()-10-MenuCloseButton:GetWide(), 10 )
	MenuCloseButton:SetText( "" )
	MenuCloseButton.Paint = function( self2, w, h )
		if( self2:IsHovered() and !self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight", 200 ) )	
		elseif( self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight" ) )
		else
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "PrimaryColor" ) )
		end
		
		surface.SetMaterial( DarkRPFoundation.MATERIALS.CloseMat )
		surface.DrawTexturedRect( 0, 0, w, h )
	end
	MenuCloseButton.DoClick = function()
		DermaPanel:Remove()
	end
	
	local TextEntry = vgui.Create( "DTextEntry", BackPanel )
	TextEntry:SetSize( BackPanel:GetWide()*0.75, 25 )
	TextEntry:SetPos( (BackPanel:GetWide()-TextEntry:GetWide())/2, 45+50 )
	TextEntry:SetText( default )
	TextEntry.OnEnter = function( self )
		chat.AddText( self:GetValue() )
	end
	
	local TextEntryXPos, TextEntryYPos = TextEntry:GetPos()
	
	local YesButton = vgui.Create( "DButton", BackPanel )
	YesButton:SetSize( (TextEntry:GetWide()-20)/2, 50 )
	YesButton:SetPos( TextEntryXPos, BackPanel:GetTall()-15-YesButton:GetTall()-5 )
	YesButton:SetText( "" )
	YesButton.Paint = function( self2, w, h )
		if( self2:IsHovered() and !self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight", 200 ) )	
		elseif( self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight" ) )
		else
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "SecondaryColor" ) )
		end
		
		surface.DrawRect( 0, 0, w, h )
		
		draw.SimpleText( confirmText or "OK", "DarkRPFoundation_Font_Lvl_PlyNameXXXS", w/2, h/2, Color( 245, 245, 245 ), TEXT_ALIGN_CENTER, TEXT_ALIGN_CENTER )
	end
	YesButton.DoClick = function()
		DermaPanel:Remove()
		func_confirm( TextEntry:GetValue() )
	end	
	
	local NoButton = vgui.Create( "DButton", BackPanel )
	NoButton:SetSize( (TextEntry:GetWide()-20)/2, 50 )
	NoButton:SetPos( TextEntryXPos+YesButton:GetWide()+20, BackPanel:GetTall()-15-NoButton:GetTall()-5 )
	NoButton:SetText( "" )
	NoButton.Paint = function( self2, w, h )
		if( self2:IsHovered() and !self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight", 200 ) )	
		elseif( self2:IsDown() ) then
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "Highlight" ) )
		else
			surface.SetDrawColor( DarkRPFoundation.DRAW.GetTheme( "SecondaryColor" ) )
		end
		
		surface.DrawRect( 0, 0, w, h )
		
		draw.SimpleText( cancelText or "Cancel", "DarkRPFoundation_Font_Lvl_PlyNameXXXS", w/2, h/2, Color( 245, 245, 245 ), TEXT_ALIGN_CENTER, TEXT_ALIGN_CENTER )
	end
	NoButton.DoClick = function()
		DermaPanel:Remove()
		func_cancel( TextEntry:GetValue() )
	end
end













